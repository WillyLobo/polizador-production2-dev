class Command(BaseCommand):
    def handle(self, *args, **kwargs):
        """
        Checks if solicitud.solicitud_actuacion is correct.
        """
        decretos = InstrumentosLegalesDecretos.objects.all()
        for decreto in decretos:
            # self.stdout.write(self.style.SUCCESS(actuacion.solicitud_actuacion))
            decreto.instrumentolegaldecretos_numero = str(decreto.instrumentolegaldecretos_numero).zfill(5)
            decreto.save()
            print(f"Decreto = {decreto.instrumentolegaldecretos_numero} -> {str(decreto.instrumentolegaldecretos_numero).zfill(5)}")

        resoluciones = InstrumentosLegalesResoluciones.objects.all()
        for resolucion in resoluciones:
            resolucion.instrumentolegalresoluciones_numero = str(resolucion.instrumentolegalresoluciones_numero).zfill(5)
            resolucion.save()
